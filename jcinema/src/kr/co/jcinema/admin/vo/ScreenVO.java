package kr.co.jcinema.admin.vo;

public class ScreenVO {
	private int screen_no;
	private String screen_name;
	private int screen_total_seat;
	private int screen_theater_no;
	
	public int getScreen_no() {
		return screen_no;
	}
	public void setScreen_no(int screen_no) {
		this.screen_no = screen_no;
	}
	public String getScreen_name() {
		return screen_name;
	}
	public void setScreen_name(String screen_name) {
		this.screen_name = screen_name;
	}
	public int getScreen_total_seat() {
		return screen_total_seat;
	}
	public void setScreen_total_seat(int screen_total_seat) {
		this.screen_total_seat = screen_total_seat;
	}
	public int getScreen_theater_no() {
		return screen_theater_no;
	}
	public void setScreen_theater_no(int screen_theater_no) {
		this.screen_theater_no = screen_theater_no;
	}
	
	
}
